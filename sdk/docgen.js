const SUPPORTED_LANGS = Object.keys(ANNEX_IV_LOCALES);
const SUPPORTED_FORMATS = ['markdown', 'html', 'json'];

function mdToHtml(md, titleText) {
  const safe = s => String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
  const html = safe(md)
    .replace(/^# (.+)$/gm, '<h1>$1</h1>')
    .replace(/^## (.+)$/gm, '<h2>$1</h2>')
    .replace(/^### (.+)$/gm, '<h3>$1</h3>')
    .replace(/^&gt; ⚠ (.+)$/gm, '<blockquote class="warn">⚠ $1</blockquote>')
    .replace(/^&gt; (.+)$/gm, '<blockquote>$1</blockquote>')
    .replace(/^---$/gm, '<hr/>')
    .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
    .replace(/_\[fill in\]_/g, '<em class="todo">[fill in]</em>')
    .replace(/_(.+?)_/g, '<em>$1</em>')
    .replace(/^- (.+)$/gm, '<li>$1</li>')
    .replace(/(<li>.*<\/li>\n?)+/g, m => '<ul>' + m + '</ul>')
    .split(/\n\n+/).map(p => p.trim().startsWith('<') ? p : (p ? '<p>' + p + '</p>' : '')).join('\n');
  return `<!doctype html>
<html><head><meta charset="utf-8"><title>${safe(titleText)}</title>
<style>
:root{--ink:#1a1a22;--accent:#8b1a1a;--soft:#5a5a62}
body{font:14px/1.65 -apple-system,system-ui,"Segoe UI",serif;color:var(--ink);background:#fff;max-width:780px;margin:32px auto;padding:0 20px}
h1{font:700 26px/1.1 'Times New Roman',serif;border-bottom:2px solid var(--accent);padding-bottom:8px;margin-top:32px}
h2{font:600 18px/1.2 'Times New Roman',serif;color:var(--accent);margin-top:28px}
h3{font:600 15px/1.2 'Times New Roman',serif;margin-top:18px}
hr{border:0;border-top:1px solid #ddd;margin:18px 0}
blockquote{border-left:3px solid var(--accent);padding:6px 14px;color:var(--soft);font-style:italic}
blockquote.warn{border-color:#c4990a;background:#fffbe6;color:#5a3d00}
em.todo{color:#999}
ul{margin:6px 0;padding-left:22px}
@media print{body{margin:0;max-width:none;padding:24mm 18mm}}
</style></head><body>
${html}
</body></html>`;
}

function buildEnvelope(values, language, opts) {
  return {
    kind: 'fall-euaiact-annexiv-v1',
    spec: 'Regulation (EU) 2024/1689',
    language,
    generated: opts.date || new Date().toISOString().slice(0, 10),
    systemName: values.system_name || null,
    provider: values.provider || null,
    purpose: values.purpose || null,
    intendedUsers: values.users || null,
    architecture: values.architecture || null,
    trainingData: values.training_data || null,
    dataGovernance: values.data_governance || null,
    riskManagement: values.risk_mgmt || null,
    accuracy: values.accuracy || null,
    humanOversight: values.human_oversight || null,
    cybersecurity: values.cybersecurity || null,
    logging: values.logging || null,
    deployerInstructions: values.instructions || null,
    postMarketMonitoring: values.post_market || null,
    fields: DOC_FIELDS.map(f => ({ id: f.id, value: values[f.id] || null, articleRef: f.art })),
  };
}

/**
 * Factory: build an Annex IV documentation object with .export() + .sign().
 * Backward-compatible · generateAnnexIV() still works for markdown-only use.
 *
 * @example
 *   import { createAnnexIV } from '@sjgant80-hub/fall-euaiact';
 *   const doc = createAnnexIV({
 *     system_name: 'My AI v2',
 *     provider: 'Acme Ltd',
 *     purpose: 'Customer support automation',
 *   });
 *   const md   = await doc.export({ format: 'markdown', language: 'en' });
 *   const html = await doc.export({ format: 'html', language: 'de' });
 *   const env  = await doc.export({ format: 'json' });
 *   const { signature } = await doc.sign(yourEd25519PrivKeyHex);
 *
 * @param {object} spec - field values keyed by DOC_FIELDS id
 * @param {{ date?: string, draftWarning?: boolean }} [opts]
 * @returns {object} { export, sign, validate, missing, fields, toJSON, supportedLanguages }
 */
export function createAnnexIV(spec, opts = {}) {
  const values = spec || {};
  const settings = { date: opts.date, draftWarning: opts.draftWarning !== false };

  return {
    fields: DOC_FIELDS.slice(),
    toJSON() { return { values, opts: settings }; },
    missing() { return validateValues(values); },
    validate() { return { complete: this.missing().length === 0, missing: this.missing() }; },
    supportedLanguages: SUPPORTED_LANGS,

    async export(exportOpts = {}) {
      const format = exportOpts.format || 'markdown';
      const lang = exportOpts.language || 'en';
      if (!SUPPORTED_FORMATS.includes(format)) throw new Error('format must be one of: ' + SUPPORTED_FORMATS.join(', '));
      if (!SUPPORTED_LANGS.includes(lang)) throw new Error('language must be one of: ' + SUPPORTED_LANGS.join(', ') + ' (community translations welcome via PR to ANNEX_IV_LOCALES)');
      const locale = ANNEX_IV_LOCALES[lang];
      let md = generateAnnexIV(values, settings);
      if (lang !== 'en') {
        md = md.replace('Technical Documentation · EU AI Act Annex IV', locale.title);
        md = md.replace(/⚠ This is a starting draft\. Review with qualified counsel before use as official conformity documentation\./, locale.draftWarn);
        md = md.replace('## 1 · General description', '## 1 · ' + locale.sect1);
        md = md.replace('## 2 · Description of system elements', '## 2 · ' + locale.sect2);
        md = md.replace('## 3 · Instructions for deployers (Article 13)', '## 3 · ' + locale.sect3 + ' (Article 13)');
        md = md.replace('## 4 · Post-market monitoring & incident reporting (Articles 72-73)', '## 4 · ' + locale.sect4 + ' (Articles 72-73)');
        md = md.replace('## 5 · EU Declaration of Conformity (Article 47)', '## 5 · ' + locale.sect5 + ' (Article 47)');
      }
      if (format === 'markdown') return md;
      if (format === 'html') return mdToHtml(md, locale.title);
      if (format === 'json') return buildEnvelope(values, lang, settings);
    },

    async sign(privateKeyHex) {
      const { sign } = await import('./sign.js');
      const env = buildEnvelope(values, 'en', settings);
      const signature = await sign(JSON.stringify(env), privateKeyHex);
      return { envelope: env, signature };
    },
  };
}
