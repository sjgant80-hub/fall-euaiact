/**
 * Generate an Annex IV technical documentation Markdown file from field values.
 * @param {object} values - keyed by DOC_FIELDS id
 * @param {{ date?: string, draftWarning?: boolean }} opts
 * @returns {string} markdown document
 */
export function generateAnnexIV(values, opts = {}) {
  values = values || {};
  const date = opts.date || new Date().toISOString().slice(0, 10);
  const draftWarning = opts.draftWarning !== false;
  const v = id => values[id] || '_[fill in]_';

  let md = `# Technical Documentation · EU AI Act Annex IV\n\n`;
  md += `> Generated: ${date} · per Regulation (EU) 2024/1689\n\n`;
  if (draftWarning) {
    md += `> ⚠ This is a starting draft. Review with qualified counsel before use as official conformity documentation.\n\n`;
  }
  md += `---\n\n## 1 · General description\n\n`;
  md += `**System name & version:** ${v('system_name')}\n\n`;
  md += `**Provider:** ${v('provider')}\n\n`;
  md += `**Intended purpose:** ${v('purpose')}\n\n`;
  md += `**Intended users + deployment context:** ${v('users')}\n\n`;
  md += `---\n\n## 2 · Description of system elements\n\n`;
  md += `### 2.1 Architecture & components\n\n${v('architecture')}\n\n`;
  md += `### 2.2 Training data (Article 10)\n\n${v('training_data')}\n\n`;
  md += `### 2.3 Data governance & bias examination\n\n${v('data_governance')}\n\n`;
  md += `### 2.4 Risk management system (Article 9)\n\n${v('risk_mgmt')}\n\n`;
  md += `### 2.5 Accuracy, robustness & cybersecurity (Article 15)\n\n**Accuracy:** ${v('accuracy')}\n\n`;
  md += `**Cybersecurity:** ${v('cybersecurity')}\n\n`;
  md += `### 2.6 Human oversight (Article 14)\n\n${v('human_oversight')}\n\n`;
  md += `### 2.7 Logging (Article 12)\n\n${v('logging')}\n\n`;
  md += `---\n\n## 3 · Instructions for deployers (Article 13)\n\n${v('instructions')}\n\n`;
  md += `---\n\n## 4 · Post-market monitoring & incident reporting (Articles 72-73)\n\n${v('post_market')}\n\n`;
  md += `---\n\n## 5 · EU Declaration of Conformity (Article 47)\n\n`;
  md += `_To be signed by the provider once all other sections are complete and verified by qualified counsel._\n\n`;
  md += `- Name & address of provider: _[fill in]_\n`;
  md += `- AI system identification: ${v('system_name')}\n`;
  md += `- Declaration of conformity with Regulation (EU) 2024/1689\n`;
  md += `- Harmonised standards applied: _[list if applicable]_\n`;
  md += `- Date · Place · Signature\n\n`;
  md += `---\n\n_Generated by fall-euaiact SDK · MIT · github.com/sjgant80-hub/fall-euaiact_\n`;
  return md;
}

export function getDocFields() { return DOC_FIELDS.slice(); }

export function validateValues(values) {
  return DOC_FIELDS.filter(f => !values?.[f.id] || !values[f.id].trim()).map(f => f.id);
}

