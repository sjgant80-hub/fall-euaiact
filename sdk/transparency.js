/**
 * Article 50 of the EU AI Act requires AI systems that interact with humans
 * to disclose their AI nature. Five categories are covered by this SDK:
 *
 *   · chatbot          · the system converses with the user
 *   · generated-text   · text content not authored by a human
 *   · generated-image  · synthetic or substantially modified images
 *   · audio            · synthetic voice or substantially modified audio
 *   · video            · synthetic or deepfake video
 *
 * The compliance bar is in Article 50(1): "in a clear and distinguishable
 * manner at the latest at the time of the first interaction or exposure."
 * That's it. The Act does NOT specify what a badge looks like · it specifies
 * what it must ACHIEVE. So we ship a sensible default that meets the bar
 * and let the consumer style it.
 */
