const SUPPORTED_LANGS = Object.keys(DEFAULT_LABELS);

function esc(s) { return String(s == null ? '' : s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;'); }

/**
 * Factory: build an Article 50 transparency badge with HTML rendering,
 * DOM mounting, impression-logging, and audit-export.
 *
 * @example
 *   import { createTransparencyBadge } from '@sjgant80-hub/fall-euaiact';
 *   const badge = createTransparencyBadge({
 *     category: 'chatbot',
 *     language: 'en',
 *     systemId: 'support-bot-v3',
 *     optOutUrl: '/preferences/turn-off-ai'
 *   });
 *   // in the browser
 *   badge.mount('#chat-header');
 *   // log impressions (Article 50 has no explicit logging duty but Article 26 does)
 *   await badge.recordImpression('user-12345');
 *   // export the log for the regulator
 *   const log = await badge.export({ from: '2026-01-01' });
 *
 * @param {object} spec
 * @param {string} spec.category - one of ARTICLE_50_CATEGORIES
 * @param {string} [spec.language='en'] - BCP-47 language code (en/de/fr/es/it/nl)
 * @param {string} [spec.systemId] - your AI system identifier (recorded with impressions)
 * @param {string} [spec.optOutUrl] - where the user can request a human (if applicable)
 * @param {string} [spec.customLabel] - override the default disclosure label
 * @param {object} [spec.styleVars] - { bg, fg, accent } CSS color overrides
 * @returns {object} { html, mount, recordImpression, export, sign, toJSON, supportedLanguages }
 */
export function createTransparencyBadge(spec) {
  spec = spec || {};
  if (!spec.category || !ARTICLE_50_CATEGORIES.includes(spec.category)) {
    throw new Error('category must be one of: ' + ARTICLE_50_CATEGORIES.join(', '));
  }
  const lang = spec.language || 'en';
  if (!SUPPORTED_LANGS.includes(lang)) {
    throw new Error('language must be one of: ' + SUPPORTED_LANGS.join(', ') + ' (PR more locales to DEFAULT_LABELS)');
  }
  const labels = DEFAULT_LABELS[lang];
  const label = spec.customLabel || labels[spec.category];
  const systemId = spec.systemId || 'unknown';
  const optOutUrl = spec.optOutUrl || null;
  const style = Object.assign({ bg: '#11131a', fg: '#e8e8ea', accent: '#b8974a' }, spec.styleVars || {});

  // ─ in-memory impression log · v1 simple · v2 will integrate IndexedDB
  const impressions = [];

  return {
    /** SUPPORTED LANGUAGE LIST · for diagnostics */
    supportedLanguages: SUPPORTED_LANGS,

    /** Round-trip the spec for storage */
    toJSON() { return { category: spec.category, language: lang, systemId, optOutUrl, label }; },

    /**
     * Render the disclosure as an HTML string.
     * Accessible · role=note · screen-reader-friendly · A11y-compliant.
     *
     * @param {{ compact?: boolean }} [opts]
     * @returns {string} HTML string
     */
    html(opts = {}) {
      const compact = !!opts.compact;
      const optOutHtml = optOutUrl ? ` <a href="${esc(optOutUrl)}" style="color:${style.accent};text-decoration:underline">${esc(labels.optOut)}</a>` : '';
      return `<div class="ai-disclosure" role="note" aria-label="AI disclosure · Article 50 EU AI Act" data-system-id="${esc(systemId)}" data-category="${esc(spec.category)}" data-language="${esc(lang)}" style="
display:inline-flex;align-items:center;gap:8px;
background:${esc(style.bg)};color:${esc(style.fg)};
font:${compact ? '11px' : '12.5px'}/1.4 -apple-system,system-ui,sans-serif;
padding:${compact ? '3px 9px' : '6px 12px'};border-radius:14px;
border:1px solid ${esc(style.accent)};letter-spacing:.2px">
<span aria-hidden="true" style="color:${esc(style.accent)};font-weight:700">◊</span>
<span>${esc(label)}${optOutHtml}</span>
</div>`;
    },

    /**
     * Mount the disclosure into a DOM element (browser only).
     * @param {string|Element} selectorOrEl
     * @param {object} [opts] - passed through to html()
     */
    mount(selectorOrEl, opts = {}) {
      if (typeof document === 'undefined') throw new Error('mount() requires a browser environment');
      const el = typeof selectorOrEl === 'string' ? document.querySelector(selectorOrEl) : selectorOrEl;
      if (!el) throw new Error('mount target not found: ' + selectorOrEl);
      const wrapper = document.createElement('div');
      wrapper.innerHTML = this.html(opts);
      el.appendChild(wrapper.firstChild);
      return el;
    },

    /**
     * Record that the disclosure was shown to a user. Article 50 doesn't
     * explicitly require this · but Article 26 (deployer record-keeping)
     * benefits from it, and your post-market monitoring (Articles 72-73)
     * will eventually need impression counts.
     *
     * @param {string} userId
     * @param {object} [meta]
     * @returns {Promise<{ ts: number, userId: string, systemId: string, category: string, language: string }>}
     */
    async recordImpression(userId, meta = {}) {
      const entry = { ts: Date.now(), userId: String(userId || 'anonymous'), systemId, category: spec.category, language: lang, ...meta };
      impressions.push(entry);
      return entry;
    },

    /**
     * Export the impression log for a date range.
     * @param {{ from?: string, to?: string }} [range] ISO dates
     * @returns {Promise<Array<object>>}
     */
    async export(range = {}) {
      const fromTs = range.from ? Date.parse(range.from) : 0;
      const toTs = range.to ? Date.parse(range.to) : Date.now();
      return impressions.filter(e => e.ts >= fromTs && e.ts <= toTs);
    },

    /**
     * Sign the badge configuration · proves you ARE running disclosure for
     * this specific system at this specific time. Useful evidence layer
     * for the post-market monitoring portfolio.
     *
     * @param {string} privateKeyHex
     * @returns {Promise<{ envelope: object, signature: string }>}
     */
    async sign(privateKeyHex) {
      const { sign } = await import('./sign.js');
      const envelope = {
        kind: 'fall-euaiact-article50-badge-v1',
        category: spec.category,
        language: lang,
        systemId,
        labelHash: await hashLabel(label),
        recordedAt: new Date().toISOString(),
        impressionsCount: impressions.length,
      };
      const signature = await sign(JSON.stringify(envelope), privateKeyHex);
      return { envelope, signature };
    },
  };
}

async function hashLabel(text) {
  if (typeof crypto !== 'undefined' && crypto.subtle) {
    const buf = await crypto.subtle.digest('SHA-256', new TextEncoder().encode(text));
    return [...new Uint8Array(buf)].map(b => b.toString(16).padStart(2, '0')).join('');
  }
  // Node fallback
  const { createHash } = await import('node:crypto');
  return createHash('sha256').update(text).digest('hex');
}
